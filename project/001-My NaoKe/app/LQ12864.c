void LCD_WrDat(unsigned char dat)
{
	unsigned char i=8;
	// //LCD_CS=0;;
	//LCD_DC=1;;
  LPLD_GPIO_Output_b(LCD_PORT,LCD_DC,1);;
  //LCD_SCL=0;;
  LPLD_GPIO_Output_b(LCD_PORT,LCD_SCL,0);;
  // //;;
  while(i--)
  {
    if(dat&0x80){
      //LCD_SDA=1;
      LPLD_GPIO_Output_b(LCD_PORT,LCD_SDA,1);
    }
    else{
      //LCD_SDA=0;
      LPLD_GPIO_Output_b(LCD_PORT,LCD_SDA,0);
    }
    //LCD_SCL=1;
    LPLD_GPIO_Output_b(LCD_PORT,LCD_SCL,1);
    ;;;
		//;;
    //LCD_SCL=0;;
    LPLD_GPIO_Output_b(LCD_PORT,LCD_SCL,0);;
    dat<<=1;
  }
	//LCD_CS=1;
}
void LCD_WrCmd(unsigned char cmd)
{
	unsigned char i=8;

	// //LCD_CS=0;;
	//LCD_DC=0;;
  LPLD_GPIO_Output_b(LCD_PORT,LCD_DC,0);;
  //LCD_SCL=0;;
  LPLD_GPIO_Output_b(LCD_PORT,LCD_SCL,0);;
  // //;;
  while(i--)
  {
    if(cmd&0x80){
      //LCD_SDA=1;
      LPLD_GPIO_Output_b(LCD_PORT,LCD_SDA,1);
    }
    else{
     // LCD_SDA=0;;
      LPLD_GPIO_Output_b(LCD_PORT,LCD_SDA,0);;
    }
    //LCD_SCL=1;;
    LPLD_GPIO_Output_b(LCD_PORT,LCD_SCL,1);;
    //LCD_SCL=0;;
    LPLD_GPIO_Output_b(LCD_PORT,LCD_SCL,0);;
    cmd<<=1;;
  }
	//LCD_CS=1;
}
void LCD_Set_Pos(unsigned char x, unsigned char y)
{
  LCD_WrCmd(0xb0+y);
  LCD_WrCmd(((x&0xf0)>>4)|0x10);
  LCD_WrCmd((x&0x0f)|0x01);
}
void LCD_Fill(unsigned char bmp_dat)
{
	unsigned char y,x;

	for(y=0;y<8;y++)
	{
		LCD_WrCmd(0xb0+y);
		LCD_WrCmd(0x01);
		LCD_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++)
			LCD_WrDat(bmp_dat);
	}
}

